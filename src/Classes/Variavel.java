package Classes;

public class Variavel {
	
	private String nome;
	
	private int universoStart;
	private int universoEnd;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getUniversoEnd() {
		return universoEnd;
	}
	public void setUniversoEnd(int universoEnd) {
		this.universoEnd = universoEnd;
	}
	public int getUniversoStart() {
		return universoStart;
	}
	public void setUniversoStart(int universoStart) {
		this.universoStart = universoStart;
	}

	
}
